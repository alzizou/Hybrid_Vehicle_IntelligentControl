% Feature Normalization
function out=feat_norm(feat)
q=length(feat);
out=zeros(q,1);
out(1)=feat(1)/50;% average speed (km/h)
out(2)=feat(2)/100;% max speed (km/h)
out(3)=feat(3)/100;% % of time spd in 0-10
out(4)=feat(4)/100;% % of time spd in 10-20
out(5)=feat(5)/100;% % of time spd in 20-30
out(6)=feat(6)/100;% % of time spd in 30-40
out(7)=feat(7)/100;% % of time spd in 40-50
out(8)=feat(8)/100;% % of time spd in 50-up
out(9)=feat(9)/1;% avg_acc
out(10)=feat(10)/6;% max_acc
out(11)=feat(11)/100;% % of time acc in 0-0.2
out(12)=feat(12)/100;% % of time acc in 0.2-0.4
out(13)=feat(13)/100;% % of time acc in 0.4-0.6
out(14)=feat(14)/100;% % of time acc in 0.6-0.8
out(15)=feat(15)/100;% % of time acc in 0.8-1
out(16)=feat(16)/100;% % of time acc in 1-up
out(17)=feat(17)/1;% avg_dec
out(18)=feat(18)/5;% max_dec
out(19)=feat(19)/100;% % of time dec in 0- -0.2
out(20)=feat(20)/100;% % of time dec in -0.2- -0.4
out(21)=feat(21)/100;% % of time dec in -0.4- -0.6
out(22)=feat(22)/100;% % of time dec in -0.6- -0.8
out(23)=feat(23)/100;% % of time dec in -0.8- -1
out(24)=feat(24)/100;% % of time dec in -1- down
out(25)=feat(25)/0.2;% RPA
out(26)=feat(26)/100;% % of time av in 0-5
out(27)=feat(27)/100;% % of time av in -5-0
out(28)=feat(28)/100;% % of time dec in 5-10
out(29)=feat(29)/100;% % of time dec in -5--10
out(30)=feat(30)/100;% % of time dec in 10-up
out(31)=feat(31)/100;% % of time dec in -10-down
out(32)=feat(32)/10;% number of stops
out(33)=feat(33)/10;% number of stops per km
out(34)=feat(34)/2;% PKE