function out=cyc_5_gen(inp)
q=length(inp);
out(1:(5*q),1)=(0:(5*q-1))';
out(1:(1*(q-1))+1,2)=inp(:,2);
out((1*(q-1))+2:(1*(q-1))+2+(q-1),2)=inp(:,2);
out((2*(q-1))+3:(2*(q-1))+3+(q-1),2)=inp(:,2);
out((3*(q-1))+4:(3*(q-1))+4+(q-1),2)=inp(:,2);
out((4*(q-1))+5:(4*(q-1))+5+(q-1),2)=inp(:,2);
end
